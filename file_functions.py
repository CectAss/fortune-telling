from random import choice

#-------------------------------------------------------------------------------------------------- Перезапись массива ---------------------------------------------------------------------------------------------------------------------------------------------- 

def rewrite(file_name):
    arr = []
    with open(file_name) as file:
        for line in file:
            arr.append(line[:-1])
    return arr

#-------------------------------------------------------------------------------------------------- Выбор из массива ---------------------------------------------------------------------------------------------------------------------------------------------- 

def gen_a_lot_from_arr(arr, n):
    return ''.join([choice(arr)+" " for i in range(n)])